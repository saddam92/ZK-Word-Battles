// Daily words - one for each day (rotates through the list)
export const getDailyWord = (date: Date = new Date()): string => {
  const startDate = new Date('2024-01-01');
  const daysSinceStart = Math.floor((date.getTime() - startDate.getTime()) / (1000 * 60 * 60 * 24));
  const wordIndex = daysSinceStart % VALID_WORDS.length;
  return VALID_WORDS[wordIndex].toUpperCase();
};

// Get the daily word hash (for commitment)
export const getDailyWordHash = (date: Date = new Date()): string => {
  const word = getDailyWord(date);
  // Simple hash for demo - in production use proper cryptographic hash
  let hash = 0;
  for (let i = 0; i < word.length; i++) {
    const char = word.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  return Math.abs(hash).toString(16).padStart(8, '0');
};

export const isValidWord = (word: string): boolean => {
  return VALID_WORDS.includes(word.toLowerCase());
};

